/*2. В первый день спортсмен пробежал $x километров,
а затем он каждый день увеличивал пробег на 10% от предыдущего значения.
По заданному числу $y определить номер дня,
на который пробег спортсмена составит не менее $y километров.*/

function dayKm ($x, $y) {
  $x1 = $x;
  $n = 1;
  while ($x < $y) {
    $x = $x*1.1;
    $n++;
  }
  echo "<br> Задание 2.<br> $n дн. для увеличения ежедневной дистанции
  от $x1 км до $y км при наращивании 10 % в день";
  return $n;
}
