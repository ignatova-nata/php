/* Задача на while или for. Дано число $num=800.
 Делите его на 2 столько раз, пока результат деления не станет меньше 50.
 Какое число получится?
 Посчитайте количество итераций, необходимых для этого
(итерация - это проход цикла).*/
function divNum ($num) {
  $x1 = $num;
  $n = 0;
  while ($num >= 50) {
    $num = $num/2;
    $n++;
  }
  echo "<br> Задание 3.<br>Дано число $x1.
   Делим его на 2  - $n раз, пока результат деления не станет меньше 50.
   Получается число - $num";
  return $n;
}
divNum(800);

